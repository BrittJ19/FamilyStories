package com.techelevator.model;

public class Total {
    private int pagesRead;
    private int bookCompleted;
    private double moneyEarned;
    private double moneyDonated;

    public int getPagesRead() {
        return pagesRead;
    }

    public void setPagesRead(int pagesRead) {
        this.pagesRead = pagesRead;
    }

    public int getBookCompleted() {
        return bookCompleted;
    }

    public void setBookCompleted(int bookCompleted) {
        this.bookCompleted = bookCompleted;
    }

    public double getMoneyEarned() {
        return moneyEarned;
    }

    public void setMoneyEarned(double moneyEarned) {
        this.moneyEarned = moneyEarned;
    }

    public double getMoneyDonated() {
        return moneyDonated;
    }

    public void setMoneyDonated(double moneyDonated) {
        this.moneyDonated = moneyDonated;
    }
}


